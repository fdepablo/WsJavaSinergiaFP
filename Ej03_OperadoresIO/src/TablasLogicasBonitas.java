public class TablasLogicasBonitas {
    public static void main(String[] args) {
        
        // ===================== TABLA OR =====================
        System.out.println("----------------------------------------------------------------------------------------------------");
        System.out.println("|                                         OR          TABLE                                        |");
        System.out.println("----------------------------------------------------------------------------------------------------");
        System.out.println("|              A                 |              B                |            A OR B              |");
        System.out.println("----------------------------------------------------------------------------------------------------");
        System.out.println("|              TRUE              |              TRUE             |              TRUE              |");
        System.out.println("----------------------------------------------------------------------------------------------------");
        System.out.println("|              TRUE              |              FALSE            |              TRUE              |");
        System.out.println("----------------------------------------------------------------------------------------------------");
        System.out.println("|              FALSE             |              TRUE             |              TRUE              |");
        System.out.println("----------------------------------------------------------------------------------------------------");
        System.out.println("|              FALSE             |              FALSE            |              FALSE             |");
        System.out.println("----------------------------------------------------------------------------------------------------\n");


        // ===================== TABLA AND =====================
        System.out.println("----------------------------------------------------------------------------------------------------");
        System.out.println("|                                        AND          TABLE                                       |");
        System.out.println("----------------------------------------------------------------------------------------------------");
        System.out.println("|              A                 |              B                |            A AND B             |");
        System.out.println("----------------------------------------------------------------------------------------------------");
        System.out.println("|              TRUE              |              TRUE             |              TRUE              |");
        System.out.println("----------------------------------------------------------------------------------------------------");
        System.out.println("|              TRUE              |              FALSE            |              FALSE             |");
        System.out.println("----------------------------------------------------------------------------------------------------");
        System.out.println("|              FALSE             |              TRUE             |              FALSE             |");
        System.out.println("----------------------------------------------------------------------------------------------------");
        System.out.println("|              FALSE             |              FALSE            |              FALSE             |");
        System.out.println("----------------------------------------------------------------------------------------------------\n");


        // ===================== TABLA NEGACIÃ“N =====================
        System.out.println("----------------------------------------------------------------");
        System.out.println("|                   NEGATION          TABLE                    |");
        System.out.println("----------------------------------------------------------------");
        System.out.println("|              A               |             NOT A             |");
        System.out.println("----------------------------------------------------------------");
        System.out.println("|              TRUE            |             FALSE             |");
        System.out.println("----------------------------------------------------------------");
        System.out.println("|              FALSE           |             TRUE              |");
        System.out.println("----------------------------------------------------------------");

        // ===================== FINAL =====================
        System.out.println("\n----------------------------------------------------------------------------------------------------");
        System.out.println("|                                   END OF LOGIC TABLES - THANK YOU                                |");
        System.out.println("----------------------------------------------------------------------------------------------------");
    }
}
