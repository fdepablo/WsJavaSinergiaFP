package ejercicio_while;

public class Ejercicio01 {

	public static void main(String[] args) {
		//Este seria un caso claro de utilizar for
		//ya que el número de veces que hay que iterar es 10
		//pero nos lo piden con while
		
		int i = 1;
		while(i <= 10) {
			System.out.println("Imprimimos el número " + i);
			i++;
		}

	}

}
